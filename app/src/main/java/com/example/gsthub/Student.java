package com.example.gsthub;

public class Student
{
    public String sName,sEmail,sYear,sBranch,sTeam;

    public Student()
    {
    }

    public Student(String sName,String sEmail,String sYear,String sBranch,String sTeam )
    {
       this.sName=sName;
       this.sEmail=sEmail;
       this.sYear=sYear;
       this.sBranch=sBranch;
       this.sTeam=sTeam;
    }
}
